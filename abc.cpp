#include <bits/stdc++.h>
using namespace std;

int main() {
	int a,b;
	cin >> a >> b;

	for (int a = 0; b <= 1; a++) {
		if (a%2 == 0) {
			cout << "even" << endl;
		} else {
			cout << "odd" << endl;
		}
	} if (b%2 == 0) {
		cout << "even" << endl;
	} else {
		cout << "odd" << endl;
	}
	return 0;
}